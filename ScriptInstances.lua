local joaat <const> = util.joaat

local GET_NUMBER_OF_THREADS_RUNNING_THE_SCRIPT_WITH_THIS_HASH=function(...)return native_invoker.uno_int(0x2C83A9DA6BFFC4F9,...)end

function is_script_running(script_name)
	return (
        GET_NUMBER_OF_THREADS_RUNNING_THE_SCRIPT_WITH_THIS_HASH(
            joaat(script_name)
        ) > 0
    ) and true or false
end

function extract_last_name(str)
    local clean_str = str:gsub("%..+$", "") -- Remove file extension
    local last_name = clean_str:match("[^\\]+$") -- Extract last item after "\\"
    return last_name or str
end

function pluralize(word, count)
    if count <= 1 then
        return word
    else
        return word .. "s"
    end
end

